class Box
{
    function lineHor($char='█', $side=20)
    {
        $sq = '';
        for($x=0;$x<$side;$x++){
            $sq .= $char;
        }
        return $sq.PHP_EOL;
    }

    function lineVer($char='█', $side=18)
    {
        $sq = '';
        $sp = '';
        for($x=0;$x<$side;$x++){
            $sp .= ' ';
        }

        for($x=0;$x<$side;$x++){
            $sq .= $char;
            $sq .= $sp.$char.PHP_EOL;
        }
        return $sq;
    }

}
$b = new Box();

print $b->lineHor();
print $b->lineVer();
